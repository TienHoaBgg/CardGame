package com.gem.cardgame.obj;

/**
 *
 * @author gem
 */
public class SizeObj {
    private float width;
    private float height;

    public float getWidth() {
        return width;
    }

    public void setWidth(float width) {
        this.width = width;
    }

    public float getHeight() {
        return height;
    }

    public void setHeight(float height) {
        this.height = height;
    }

    public SizeObj(float width, float height) {
        this.width = width;
        this.height = height;
    }
    
}
